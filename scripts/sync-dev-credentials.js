#!/usr/bin/env node
/**
 * Sync backend/.env admin credentials into frontend/.env.development.local
 * so `npm run dev` auto-logs you in on localhost.
 */

const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const backendEnv = path.join(root, 'backend/.env');
const outPath = path.join(root, 'frontend/.env.development.local');

function parseEnv(filePath) {
  const out = {};
  if (!fs.existsSync(filePath)) return out;
  for (const line of fs.readFileSync(filePath, 'utf8').split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq <= 0) continue;
    out[t.slice(0, eq).trim()] = t.slice(eq + 1).trim();
  }
  return out;
}

const env = parseEnv(backendEnv);
const email = env.ADMIN_EMAIL || 'admin@example.com';
const password = env.ADMIN_PASSWORD || '';

const content = `# Auto-generated by npm run local:setup — gitignored, local dev only
VITE_DEV_AUTO_LOGIN=true
VITE_DEV_LOGIN_EMAIL=${email}
VITE_DEV_LOGIN_PASSWORD=${password}
`;

fs.writeFileSync(outPath, content, 'utf8');
console.log(`✓ Wrote ${outPath}`);
console.log(`  Auto-login: ${email} (localhost only)`);
